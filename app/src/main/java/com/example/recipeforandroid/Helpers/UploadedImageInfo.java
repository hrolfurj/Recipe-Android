package com.example.recipeforandroid.Helpers;

public class UploadedImageInfo {
    String filename;
    String name;
    String mime;
    String extension;
    String url;

    public String getFilename() {
        return filename;
    }

    public String getName() {
        return name;
    }

    public String getMime() {
        return mime;
    }

    public String getExtension() {
        return extension;
    }

    public String getUrl() {
        return url;
    }
}
